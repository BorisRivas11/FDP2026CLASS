#include <iostream>
using namespace std;
int main(){

    int numero;
    cin >> numero;
    cout << "Bienvenido al programa de cuenta regresiva" << endl;
    cout << "Ingrese un numero entero positivo para iniciar la cuenta regresiva" << endl;
    
    while ( numero >=0){
        cout <<"Ingresa un numero positivo" << endl;
        cout << numero << endl;
        numero--; 
    }
    cout << "La cuenta ha terminado" << endl;
    return 0;










    return 0;

}